setup = _ => {
  createCanvas(512, 512);
}

draw = _ => {
  background(220);

  generateTileMap();
}

const generateTileMap = () => {
  push();
  noStroke();
  for (let x = 0; x < tilemap.length; x++) {
    for (let y = 0; y < tilemap[x].length; y++) {

      switch (tilemap[y][x]) {
        case 0:
          fill('#030303');
          break;

        case 1:
          fill('#f8f8f8');
          break;
      }

      rect(x * tile.w, y * tile.h, tile.w, tile.h);
    }
  }
  pop();
}